# PRATICANDO COM WHILE - LAÇOS DE REPETIÇÃO

# Imprima na tela os números de 0 a 10

# Variável com o valor de inicio

i = 1

# Enquanto (i) for menor que 10, exiba o valor de (i) até chegar no 10

# Uma atenção se quero até 10 então coloco 11, ou então coloco 10 + 1 ai chega no valor desejado.

while i < 10 + 1:
    print(i)
    
# se rodar o código assim, ele vai ter um loop infinito de 1 1 1 1 1...

# Então vou acresecentar + 1 ao meu (i) assim quando verificar que 1 é menor que 10, vai ser somado + 1 ao valor de (i) que se torna 2, e assim continua o processo até chegar no valor desejado.

    i = i + 1

